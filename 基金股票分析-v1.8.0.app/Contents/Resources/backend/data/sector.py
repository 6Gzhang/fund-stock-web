def get_sector_stocks(sector_name: str) -> list:
    """
    返回某行业的所有股票（从stock_spot缓存中过滤）
    
    参数:
        sector_name: 行业名称
    
    返回:
        股票列表，每只股票包含 code、name、price、change_pct 等信息
    """
    if not sector_name:
        return []
    
    try:
        df = _cached("stock_spot", _fetch_stock_spot, wait=True)
        if df is None or df.empty:
            return []
        
        results = []
        for _, row in df.iterrows():
            code = str(row.get("代码", ""))
            name = str(row.get("名称", ""))
            sectors = get_sector_for_stock(code, name)
            
            if sector_name in sectors:
                results.append({
                    "code": code,
                    "name": name,
                    "price": float(row.get("最新价", 0) or 0),
                    "change": float(row.get("涨跌额", 0) or 0),
                    "change_pct": float(row.get("涨跌幅", 0) or 0),
                    "volume": float(row.get("成交量", 0) or 0),
                    "amount": float(row.get("成交额", 0) or 0),
                    "high": float(row.get("最高", 0) or 0),
                    "low": float(row.get("最低", 0) or 0),
                    "open": float(row.get("今开", 0) or 0),
                    "pre_close": float(row.get("昨收", 0) or 0),
                    "sectors": sectors,
                })
        
        results.sort(key=lambda x: x["change_pct"], reverse=True)
        return results
    
    except Exception as e:
        print(f"获取行业股票列表失败 [{sector_name}]: {e}")
        return []
