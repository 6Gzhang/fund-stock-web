# ========== 内部工具函数 ==========

def _match_keywords(name: str, keyword_map: list) -> list:
    """根据名称关键词匹配行业，返回匹配到的行业列表（去重、保持顺序）"""
    if not name:
        return []
    name_clean = name.upper()
    sectors = []
    seen = set()
    
    all_matches = []
    for keywords, sector_list in keyword_map:
        for kw in keywords:
            kw_upper = kw.upper()
            if kw_upper in name_clean:
                all_matches.append((len(kw_upper), kw_upper, sector_list))
                break
    
    all_matches.sort(key=lambda x: -x[0])
    
    accepted_keywords = []
    for kw_len, kw_upper, sector_list in all_matches:
        is_substring = False
        for accepted_kw in accepted_keywords:
            if kw_upper in accepted_kw:
                is_substring = True
                break
        if not is_substring:
            accepted_keywords.append(kw_upper)
            for s in sector_list:
                if s not in seen:
                    sectors.append(s)
                    seen.add(s)
    
    return sectors


def _clean_name_for_match(name: str) -> str:
    """清理股票/ETF名称，去除前缀后缀以便匹配"""
    if not name:
        return ""
    cleaned = _clean_stock_name(str(name))
    cleaned = cleaned.replace(" ", "").replace("-", "")
    return cleaned


