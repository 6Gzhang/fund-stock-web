def get_sector_for_stock(code: str, name: str) -> list:
    """
    根据股票代码和名称判断所属行业
    
    参数:
        code: 股票代码（6位数字，可带sh/sz/bj前缀）
        name: 股票名称
    
    返回:
        行业列表（一只股票可属于多个行业）
    """
    if not code or not name:
        return []
    
    code_clean = _clean_code(str(code))
    name_clean = _clean_name_for_match(name)
    
    sectors = []
    seen = set()
    
    # 第一步：关键词匹配（优先级最高）
    keyword_sectors = _match_keywords(name_clean, _STOCK_KEYWORD_MAP)
    for s in keyword_sectors:
        if s not in seen:
            sectors.append(s)
            seen.add(s)
    
    # 第二步：代码前缀判断（补充判断）
    if len(code_clean) == 6:
        # 科创板 688开头 - 科技属性
        if code_clean.startswith("688") and "科创板" not in seen:
            # 科创板股票补充科技相关标签（如果还没有匹配到科技类行业）
            has_tech = any(s in ["半导体", "芯片", "人工智能", "软件", "计算机", "医药", "医药生物", "新材料", "高端制造", "机器人"] for s in sectors)
            if not has_tech:
                sectors.append("科创板")
                seen.add("科创板")
        
        # 创业板 300/301开头 - 成长属性
        if code_clean.startswith("300") or code_clean.startswith("301"):
            has_growth = any(s in ["半导体", "芯片", "人工智能", "软件", "计算机", "医药", "医药生物", "新能源", "锂电", "光伏", "新材料", "高端制造", "机器人"] for s in sectors)
            if not has_growth:
                sectors.append("创业板")
                seen.add("创业板")
    
    # 第三步：如果一个行业都没匹配到，根据代码前缀给默认分类
    if not sectors:
        if code_clean.startswith("688"):
            sectors = ["科创板", "科技"]
        elif code_clean.startswith("300") or code_clean.startswith("301"):
            sectors = ["创业板"]
        elif code_clean.startswith("600") or code_clean.startswith("601") or code_clean.startswith("603") or code_clean.startswith("605"):
            sectors = ["沪市主板"]
        elif code_clean.startswith("000") or code_clean.startswith("001") or code_clean.startswith("002") or code_clean.startswith("003"):
            sectors = ["深市主板"]
        elif code_clean.startswith("8") or code_clean.startswith("4"):
            sectors = ["北交所"]
    
    return sectors


